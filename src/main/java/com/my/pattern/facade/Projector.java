package com.my.pattern.facade;

public class Projector {
    public void on() {
        System.out.println("放下投影仪幕布");
    }

    public void open() {
        System.out.println("打开投影仪");
    }

    public void close() {
        System.out.println("关闭投影仪");
    }

    public void off() {
        System.out.println("收起投影仪幕布");
    }
}
